export function getLevel(id) {
  const lvl = LEVELS.find(l => l.id === id);
  if (!lvl) return null;
  // Deep-copy grid so mutations (fire extinguish, etc.) don't persist
  return {
    ...lvl,
    grid: lvl.grid.map(row => [...row]),
    links: lvl.links.map(l => ({ plate: [...l.plate], doors: l.doors.map(d => [...d]) })),
    activatedPlates: new Set(),
    openDoors: new Set(),
  };
}

